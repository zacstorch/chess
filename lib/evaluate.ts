function pieceValue(
  type: PieceType,
  color: Color,
  row: number,
  col: number,
): number {
  const tableRow = color === "w" ? row : 7 - row;
  return MATERIAL[type] + PST[type][tableRow][col];
}

function materialAndPosition(board: Board): number {
  let score = 0;
  for (let r = 0; r < 8; r++) {
    for (let c = 0; c < 8; c++) {
      const piece = board[r][c];
      if (!piece) continue;
      const value = pieceValue(piece.type, piece.color, r, c);
      score += piece.color === "w" ? value : -value;
    }
  }
  return score;
}

// Score in centipawns from White's perspective. +Infinity/-Infinity mean
// White/Black has delivered checkmate.
export function evaluate(state: GameState): number {
  const status = getGameStatus(state);
  if (status === "checkmate") return state.turn === "w" ? -Infinity : Infinity;
  if (status === "stalemate") return 0;
  return materialAndPosition(state.board);
}
